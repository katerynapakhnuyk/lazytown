package work3;

/**
 * Інтерфейс для реалізації ітератора, який дозволяє обходити елементи
 * колекції (наприклад, зв'язного списку) у послідовному порядку.
 *
 * @param <T> тип елементів, що зберігаються в колекції.
 */
public interface Iterator<T> {

    /**
     * Перевіряє, чи є наступний елемент у колекції.
     *
     * @return true, якщо наступний елемент існує; false в іншому випадку.
     */
    boolean hasNext();

    /**
     * Повертає наступний елемент у колекції.
     *
     * @return наступний елемент колекції.
     */
    T next();

    /**
     * Видаляє останній елемент, який було повернено методом next().
     *
     * @throws IllegalStateException якщо метод next() ще не був викликаний,
     * або якщо метод remove() вже був викликаний після останнього виклику next().
     */
    void remove();
}